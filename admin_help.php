      <div class="card-body">
         <div class="entry-content" style="box-sizing: border-box; outline: none !important; margin-bottom: 75px; color: rgb(100, 100, 100); font-family: Arial, Helvetica, sans-serif; font-size: 15px; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: 400; letter-spacing: normal; orphans: 2; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: 2; word-spacing: 0px; -webkit-text-stroke-width: 0px; background-color: rgb(255, 255, 255); text-decoration-style: initial; text-decoration-color: initial;">
	<p>
	<strong>
	The first way, exclude by post URL:</strong></p>
	<p>
	By using this way, all images inside that URL will not be watermarked</p>
	<p>
	<strong>
	Note</strong>: There is no need to put the full URL, you can put any part of 
	it and it will work fine</p>
	<p>
	<span style="box-sizing: border-box; outline: none !important; text-decoration: underline;">
	Example</span>: mysite.com/post-name OR www.mysite.com/post-name</p>
	<p>
	If you use any URL ends with /* then all URLs start with that URL will be 
	excluded (this called&nbsp;<strong><span style="box-sizing: border-box; outline: none !important; text-decoration: underline;">bulk 
	exclusion</span></strong>)</p>
	<p>
	<span style="box-sizing: border-box; outline: none !important; text-decoration: underline;">
	Example</span>: mysite.com/cart/*</p>
	<p>
	<img class="aligncenter size-full wp-image-15542" src="https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM.png" alt="" width="754" height="167" srcset="https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM.png 754w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-300x66.png 300w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-645x143.png 645w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-560x124.png 560w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-750x166.png 750w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-400x89.png 400w, https://www.wp-buy.com/wp-content/uploads/2020/05/5-29-2020-10-34-40-PM-228x50.png 228w" sizes="(max-width: 754px) 100vw, 754px" style="box-sizing: border-box; outline: none !important; border: 0px; vertical-align: middle; transition: background 0.3s ease 0s, border-color 0.2s ease 0s, color 0.3s ease 0s; text-align: center; clear: both; display: block; margin: 7px auto; max-width: 100%; height: auto;"></p>
	<p>
	<strong>
	The second way, exclude registered images sizes:</strong></p>
	<p>
	This way is great, it tells the plugin to not watermark any image with a 
	chosen size, this is a very effective way to reduce the use of server 
	resources and improve the speed of the page.</p>
	<p>
	We recommend choosing small sizes to be out of watermarking, for a simple 
	reason, No one wants to steal a small image!</p>
	<p>
	This feature depends on image names generated by WordPress when you uploaded 
	it, not real image sizes, if the size of the image is not a part of its 
	name, it will be watermarked</p>
	<p>
	<span style="box-sizing: border-box; outline: none !important; text-decoration: underline;">
	For example</span>: if you have an image with size 1000X1000 but its name is 
	image_150x150 then it will be treated as 150×150 image because of its name</p>
	<p>
