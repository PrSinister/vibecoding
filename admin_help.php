	<p>
	<strong>
	The third way, Exclude images by name &amp; size (manually):</strong></p>
	<p>
	Simply, Enter any image name to be excluded from watermarking 
	(Comma-separated)</p>
	<p>
	Write excluded images names or some parts of names with comma separated</p>
	<p>
	<span style="box-sizing: border-box; outline: none !important; text-decoration: underline;">
	Note</span>: The image name is enough to exclude it without its full URL</p>
